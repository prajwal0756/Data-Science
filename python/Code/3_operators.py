# Arithmetic Operators
# These operators are used to perform basic mathematical operations.
# + (Addition), - (Subtraction), * (Multiplication), / (Division),
print(a+b)  # Output: 7  #it adds to values
print(a-b)  # Output: -3  #it subtracts two values
print(a*b)  # Output: 10 3 #it multiplies two values
print(a/b)  # Output: 0.4 #it divides two values
print(a//b)  # Output: 0 #it divides two values and returns the integer part round to nearest
print(a%b)  # Output: 2 #it divides two values and returns the remainder
print(a**b)  # Output: 32 #it raises the first value to the power of the second value



# Assignment Operators# 
