# Comparison Operators
# These operators are used to compare two values and return a boolean result.
# == (Equal), != (Not equal), > (Greater than), < (Less than),
# >= (Greater than or equal to), <= (Less than or equal to)
print(a == b)   #it checks if two values are equal
print(a != b)    #it checks if two values are not equal
print(a > b)   #it checks if the first value is greater than the second value
print(a < b)    #it checks if the first value is less than the second value
print(a >= b)    #it checks if the first value is greater than or equal to the second value
print(a <= b)   #it checks if the first value is less than or equal to the second value


# Logical Operators
# These operators are used to combine conditional statements.
# and (Logical AND), or (Logical OR), not (Logical NOT)
print(a > b and c > d)    #it checks if both conditions are true
print(a > b or c > d)   #it checks if at least one condition is true
print(not (a > b))   #it negates the condition, returns True if the condition is False



